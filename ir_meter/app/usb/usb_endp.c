/*******************************************************************************
* Function Name  : EP2_OUT_Callback.
* Description    : EP2 OUT Callback Routine.
* Input          : None.
* Output         : None.
* Return         : None.
*******************************************************************************/
void EP2_OUT_Callback(void)
{
  u8 DataLen;
  DataLen = GetEPRxCount(ENDP2);
  PMAToUserBufferCopy(Out_Buffer, ENDP2_RXADDR, DataLen);
  SetEPRxValid(ENDP2);
}

